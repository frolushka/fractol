#include "ll3d.h"

t_vec4	*vec4_sum(const t_vec4 *v1, const t_vec4 *v2)
{
	t_vec4	*vr;

	if (!(vr = vec4_init()))
		return (NULL);
	vr->x = v1->x + v2->x;
	vr->y = v1->y + v2->y;
	vr->z = v1->z + v2->z;
	vr->w = v1->w + v2->w;
	return (vr);
}
